package com.fangbian365.kuaidi.base.bean;

/**
 * 改变餐桌状态类
 */
public class TableStatusChange {
	
	
	private String tingbh;//厅编号
	private String taibh;//台编号
	private int state;//状态
	
	
	public String getTingbh() {
		return tingbh;
	}


	public void setTingbh(String tingbh) {
		this.tingbh = tingbh;
	}


	public String getTaibh() {
		return taibh;
	}


	public void setTaibh(String taibh) {
		this.taibh = taibh;
	}


	public int getState() {
		return state;
	}


	public void setState(int state) {
		this.state = state;
	}


	
	

}
